	public void setBbs_name(String bbs_name) {
		this.bbs_name = bbs_name;
	}
	public String getBbs_manager() {
		return bbs_manager;
	}
	public void setBbs_manager(String bbs_manager) {
		this.bbs_manager = bbs_manager;
	}
	public String getBbs_start() {
		return bbs_start;
	}
	public void setBbs_start(String bbs_start) {
		this.bbs_start = bbs_start;
	}
	public String getBbs_arrival() {
		return bbs_arrival;
	}
	public void setBbs_arrival(String bbs_arrival) {
		this.bbs_arrival = bbs_arrival;
	}
