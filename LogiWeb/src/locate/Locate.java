package locate;

public class Locate {
	private int l_id;
	private String l_wido;
	private String l_gyeongdo;
	private String l_time;
	
	public int getL_id() {
		return l_id;
	}
	public void setL_id(int l_id) {
		this.l_id = l_id;
	}
	public String getL_wido() {
		return l_wido;
	}
	public void setL_wido(String l_wido) {
		this.l_wido = l_wido;
	}
	public String getL_gyeongdo() {
		return l_gyeongdo;
	}
	public void setL_gyeongdo(String l_gyeongdo) {
		this.l_gyeongdo = l_gyeongdo;
	}
	public String getL_time() {
		return l_time;
	}
	public void setL_time(String l_time) {
		this.l_time = l_time;
	}
}
