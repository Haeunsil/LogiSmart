package temper;

public class Temper {
	private int t_id;
	private float t_data;
	private String t_time;
	
	public int getT_id() {
		return t_id;
	}
	public void setT_id(int t_id) {
		this.t_id = t_id;
	}
	public float getT_data() {
		return t_data;
	}
	public void setT_data(float t_data) {
		this.t_data = t_data;
	}
	public String getT_time() {
		return t_time;
	}
	public void setT_time(String t_time) {
		this.t_time = t_time;
	}
}
