package bluetooth;

public class Bluetooth {
	private String b_name;
	private int b_carrier;
	private int b_thing;
	private int b_conn;
	
	public String getB_name() {
		return b_name;
	}
	public void setB_name(String b_name) {
		this.b_name = b_name;
	}
	public int getB_carrier() {
		return b_carrier;
	}
	public void setB_carrier(int b_carrier) {
		this.b_carrier = b_carrier;
	}
	public int getB_thing() {
		return b_thing;
	}
	public void setB_thing(int b_thing) {
		this.b_thing = b_thing;
	}
	public int getB_conn() {
		return b_conn;
	}
	public void setB_conn(int b_conn) {
		this.b_conn = b_conn;
	}
}
